#include "../push_swap.h"

int ft_sqrt(int n)
{
    int r;

    if (n < 0)
        return (0);
    if (n == 0 || n == 1)
        return (n);
    r = 1;
    while (r * r <= n / 2)
    {
        r++;
    }
    return (r);
}
